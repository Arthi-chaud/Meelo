/// Enumeration of the different sources of a track rip
enum RipSource {
	/**
	 * Extracted from an Audio CD
	 */
	CD,
	/**
	 * Extracted from a DVD
	 */
	DVD,
	/**
	 * Extracted from a Blu-Ray disc
	 */
	BluRay,
	/**
	 * Ripped from an Audio Tape
	 */
	Cassette,
	/**
	 * Ripped from an Vinyl Record
	 */
	Vinyl,
	/**
	 * Digital source
	 */
	Digital,
	/**
	 * Other rip source
	 */
	Other
}